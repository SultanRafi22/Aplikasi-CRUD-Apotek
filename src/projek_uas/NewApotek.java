    private void exportToCSV(String filePath) {
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_apotek", "root", "");
            Statement statement = cn.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT * FROM obat");

            // Membuat file CSV
            File file = new File(filePath);
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);

            // Menulis header ke dalam file CSV
            bw.write("ID Obat, Nama Obat, Jumlah Tersedia ,Harga Satuan");
            bw.newLine();   

            // Menulis data dari tabel ke dalam file CSV
            while (resultSet.next()) {
                String id = resultSet.getString("id_obat");
                String nama = resultSet.getString("nama_obat");
                String jumlah = resultSet.getString("jumlah");
                String harga = resultSet.getString("harga");

                bw.write(id + "," + nama + "," + jumlah + "," + harga);
                bw.newLine();
            }

            bw.close();
            fw.close();

            JOptionPane.showMessageDialog(null, "Data berhasil diekspor ke " + filePath);
        } catch (SQLException | IOException ex) {
            Logger.getLogger(NewApotek.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengekspor data");
        }
    }
    
    private void refreshTabel() {
        try {
            model.setRowCount(0);
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_apotek", "root", "");
            try {
                String query = "SELECT * FROM obat";
                PreparedStatement pst = cn.prepareStatement(query);
                ResultSet rs = pst.executeQuery();
                while (rs.next()) {
                    String id = rs.getString("id_obat");
                    String nama = rs.getString("nama_obat");
                    String jumlah = rs.getString("jumlah");
                    String harga = rs.getString("harga");
                    model.addRow(new Object[] { id, nama, jumlah, harga });
                }
                rs.close();
                pst.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewApotek.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void tampilkan() throws SQLException {
        int row = tabel.getRowCount();
        for(int a = 0; a < row; a++) {
            model.removeRow(0);
        }
        try{ 
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_apotek", "root", "");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM obat");
            while(rs.next()) {
                String[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewApotek.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }

