    private void TambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahButtonActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:  
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_apotek", "root", "");
            String id = id_field.getText();
            String nama = nama_field.getText();
            String jumlah = jumlah_field.getText();
            String harga = harga_field.getText();
            
            // Memeriksa apakah ada data yang kosong
            if (id.isEmpty() || nama.isEmpty() || jumlah.isEmpty() || harga.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ada data yang kosong");
                return; // Menghentikan eksekusi jika ada data yang kosong
            }
            
            PreparedStatement preparedStatement = cn.prepareStatement("INSERT INTO obat VALUES (?, ?, ?, ?)");

            // Mengatur parameter PreparedStatement
            preparedStatement.setString(1, id);
            preparedStatement.setString(2, nama);
            preparedStatement.setString(3, jumlah);
            preparedStatement.setString(4, harga);

            // Menjalankan pernyataan
            preparedStatement.executeUpdate();
            
            tampilkan();
            bersihkan();
        } catch (SQLException ex) {
            Logger.getLogger(NewApotek.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ada data yang belum lengkap");
        }
    }//GEN-LAST:event_TambahButtonActionPerformed

